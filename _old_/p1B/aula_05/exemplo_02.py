# Resolução do contexto da apresentação

ciclos = 5
while ciclos > 0:
    print('Por favor, altere sua senha.'
          f'\nFaltam apenas {ciclos} dias'
          f' para ela ser bloqueada.')
    print('\n')
    ciclos -= 1


for i in range(5, 0, -1):

    print('Por favor, altere sua senha.'
          f'\nFaltam apenas {i} dias'
          f' para ela ser bloqueada.')
    print('\n')