def multiplicar(numero1, numero2):
    return numero1 * numero2


def somar(numero1, numero2):
    return numero1 + numero2


def subtrair(numero1, numero2):
    return numero1 - numero2


def dividir(numero1, numero2):
    return numero1 / numero2


