# Escreva um algoritmo para imprimir os números de 1 (inclusive) a 10 (inclusive) em ordem crescente.

# Estrutura enquanto-faça (while)
x = 1
while x <= 10:
    print("While: %s" % x)
    x += 1

print('--------------------')

# Estrutura para-faça (for)
for i in range(1,11):
    print('For %s' %i)